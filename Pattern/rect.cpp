#include <iostream>
using namespace std;

int main()
/**Rectangular */

{
    int rows = 4;
    int col = 6;

    for (int i = 1; i <= rows; i++)
    {
        for (int i = 1; i <= col; i++)
        {
            cout << "*";
        }
        cout << endl;
    }

    return 0;
}

